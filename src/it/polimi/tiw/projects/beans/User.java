package it.polimi.tiw.projects.beans;

public class User {
	
	private String role;
	private String username;
	

	
	public String getRole() {
		return role;
	}

	public String getUsername() {
		return username;
	}
	

	public void setRole(String r) {
		role = r;
	}

	public void setUsername(String un) {
		username = un;
	}

}
